package reactRedux

import react.ComponentClass
import react.StatelessComponent
import scala.scalajs.js

 /**
   * Decorator that infers the type from the original component
   *
   * Can't use the above decorator because it would default the type to {}
   */
@js.native
trait InferableComponentDecorator  extends js.Any {
def component[P, TComponentConstruct <: StatelessComponent[P] ]: TComponentConstruct  = js.native
  
}

 